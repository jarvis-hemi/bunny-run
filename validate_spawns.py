    print(f"\n{level['name']}:")
    for pred in level['predators']:
        col = pred['col']
        row = pred['row']
        tile = level['map'][row][col]
        tile_name = tile_names.get(tile, f'UNKNOWN({tile})')
        is_walkable = tile not in [WALL, TREE]
        status = "✅ OK" if is_walkable else "❌ INVALID"
        if not is_walkable:
            all_valid = False
        print(f"  {pred['type']:6s} at ({col:2d}, {row:2d}) -> tile={tile:2d} ({tile_name:12s}) {status}")

